CREATE OR REPLACE VIEW CMDB.APPLICATIONDEPENDENCIES AS
SELECT
    a.NAME        AS APPLICATIONNAME,
    a.VERSION     AS APPVERSION,
    a.CRITICALITY AS CRITICALITY,
    d.NAME        AS DATABASENAME,
    d.VERSION     AS DBVERSION,
    d.ENGINE      AS ENGINE,
    d.SIZEGB      AS SIZEGB,
    ad.ACCESSTYPE AS ACCESSTYPE
FROM CMDB.APPLICATION a
JOIN CMDB.APPLICATION_DATABASE ad ON a.ID = ad.APPLICATION_ID
JOIN CMDB.DATABASEINSTANCE d      ON ad.DATABASEINSTANCE_ID = d.ID;

CREATE OR REPLACE VIEW CMDB.CAPACITYPLANNING AS
SELECT
    l.NAME    AS LOCATIONNAME,
    l.CITY    AS CITY,
    l.COUNTRY AS COUNTRY,
    COUNT(DISTINCT s.ID)  AS SERVERCOUNT,
    SUM(s.CPUCORES)       AS TOTALCPUCORES,
    SUM(s.RAMGB)          AS TOTALRAMGB,
    COUNT(DISTINCT r.ID)  AS RACKCOUNT,
    SUM(st.CAPACITYTB)    AS TOTALSTORAGECAPACITYTB,
    SUM(st.USEDTB)        AS TOTALSTORAGEUSEDTB
FROM CMDB.LOCATION l
LEFT JOIN CMDB.SERVER  s  ON l.ID = s.LOCATION_ID
LEFT JOIN CMDB.RACK    r  ON l.ID = r.LOCATION_ID
LEFT JOIN CMDB.STORAGE st ON l.ID = st.LOCATION_ID
GROUP BY l.NAME, l.CITY, l.COUNTRY;

CREATE OR REPLACE VIEW CMDB.DEPARTMENTCONTACTS AS
SELECT
    d.ID          AS DEPARTMENTID,
    d.NAME        AS DEPARTMENTNAME,
    d.COSTCENTER  AS COSTCENTER,
    d.MANAGER     AS MANAGER,
    c.NAME        AS CONTACTNAME,
    c.EMAIL       AS EMAIL,
    c.PHONE       AS PHONE,
    cd.ROLE       AS ROLE,
    cd.SINCE      AS SINCE
FROM CMDB.DEPARTMENT d
JOIN CMDB.CONTACT_DEPARTMENT cd ON d.ID = cd.DEPARTMENT_ID
JOIN CMDB.CONTACT c             ON cd.CONTACT_ID = c.ID;

CREATE OR REPLACE VIEW CMDB.NETWORKASSIGNMENT AS
SELECT
    s.NAME    AS SERVERNAME,
    s.STATUS  AS SERVERSTATUS,
    l.NAME    AS LOCATIONNAME,
    n.NAME    AS NETWORKNAME,
    n.TYPE    AS NETWORKTYPE,
    n.SUBNET  AS SUBNET,
    n.GATEWAY AS GATEWAY,
    n.VLAN    AS VLAN,
    sn.IPADDRESS  AS IPADDRESS,
    sn.MACADDRESS AS MACADDRESS
FROM CMDB.SERVER s
JOIN CMDB.SERVER_NETWORK sn ON s.ID = sn.SERVER_ID
JOIN CMDB.NETWORK n         ON sn.NETWORK_ID = n.ID
LEFT JOIN CMDB.LOCATION l   ON s.LOCATION_ID = l.ID;

CREATE OR REPLACE VIEW CMDB.SERVERAPPLICATIONS AS
SELECT
    s.NAME          AS SERVERNAME,
    s.STATUS        AS SERVERSTATUS,
    a.NAME          AS APPLICATIONNAME,
    a.VERSION       AS VERSION,
    a.CRITICALITY   AS CRITICALITY,
    sa.INSTALLEDDATE AS INSTALLEDDATE,
    sa.STATUS        AS INSTALLSTATUS
FROM CMDB.SERVER s
JOIN CMDB.SERVER_APPLICATION sa ON s.ID = sa.SERVER_ID
JOIN CMDB.APPLICATION a         ON sa.APPLICATION_ID = a.ID;

CREATE OR REPLACE VIEW CMDB.SERVERBYLOCATION AS
SELECT
    l.NAME    AS LOCATIONNAME,
    l.TYPE    AS LOCATIONTYPE,
    l.CITY    AS CITY,
    l.COUNTRY AS COUNTRY,
    COUNT(s.ID)    AS SERVERCOUNT,
    SUM(s.CPUCORES) AS TOTALCPUCORES,
    SUM(s.RAMGB)    AS TOTALRAMGB
FROM CMDB.LOCATION l
LEFT JOIN CMDB.SERVER s ON l.ID = s.LOCATION_ID
GROUP BY l.NAME, l.TYPE, l.CITY, l.COUNTRY;

